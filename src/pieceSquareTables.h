// King endgame - centralization
constexpr std::array<int, 64> KING_EG = {
   -50,-40,-30,-20,-20,-30,-40,-50,
   -30,-20,-10,  0,  0,-10,-20,-30,
   -30,-10, 20, 30, 30, 20,-10,-30,
   -30,-10, 30, 40, 40, 30,-10,-30,
   -30,-10, 30, 40, 40, 30,-10,-30,
   -30,-10, 20, 30, 30, 20,-10,-30,
   -30,-30,  0,  0,  0,  0,-30,-30,
   -50,-30,-30,-30,-30,-30,-30,-50
};

// Get piece-square value for a piece at a given square
inline int getPieceSquareValue(PieceType pieceType, Color color, int square, bool isEndgame = false) {
    // Flip square for black pieces (board is indexed from white's perspective)
    int idx = (color == Color::WHITE) ? square : (63 - square);

    switch (pieceType) {
        case PieceType::PAWN:
            return PAWN[idx];
        case PieceType::KNIGHT:
            return KNIGHT[idx];
        case PieceType::BISHOP:
            return BISHOP[idx];
        case PieceType::ROOK:
            return ROOK[idx];
        case PieceType::QUEEN:
            return QUEEN[idx];
        case PieceType::KING:
            return isEndgame ? KING_EG[idx] : KING_MG[idx];
        default:
            return 0;
    }
}

